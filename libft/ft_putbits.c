#include "libft.h"

void	ft_putbits(long l, size_t type)
{
	int		i;

	i = 8 * type;
	while (i--)
	{
		ft_putchar(((l >> i) & 1) ? '1' : '0');
		if (i % 8 == 0)
			ft_putchar(' ');
	}
	ft_putchar('\n');
}
